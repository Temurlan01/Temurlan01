def calculate_sum(number1, number2):
    result = number1 + number2
    return result


def calculate_difference(number1, number2):
    result = number1 - number2
    return result


def calculate_product(number1, number2):
    result = number1 * number2
    return result


def calculate_div(number1, number2):
    result = number1 / number2
    return result